FROM fire_characteristics
INNER JOIN shudong_flame_and_litter
ON LOWER(TRIM(BOTH ' ' FROM fire_characteristics.scientific_name)) = LOWER(TRIM(BOTH ' ' FROM shudong_flame_and_litter.species))
GROUP BY scientific_name





