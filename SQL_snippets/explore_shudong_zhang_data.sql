-- What units are given?
SELECT "C_unit", "N_unit", count("C_unit") 
FROM nutrients 
WHERE "C_unit" != '9999' 
GROUP BY "C_unit", "N_unit" 
ORDER BY count DESC

-- How many rows have measurements for C and N?  (Only two rows have "Foliage_C_%"!)
SELECT "Foliage_C_%", "Foliage_N_%", COUNT("Foliage_C_%") 
FROM nutrients 
WHERE "Foliage_C_%" != '9999'
GROUP BY "Foliage_C_%", "Foliage_N_%" 
ORDER BY count DESC

